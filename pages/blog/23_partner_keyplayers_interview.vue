            <p>今回は、高野様（<a href="https://twitter.com/keyplayers">@keyplayers</a>）へインタビューをさせていただきました。TEDxUTsukubaへの協賛理由や高野様ご自身のことについて、そして今の大学生へ向けたコメントを頂戴いたしました。</p>
                   
            <h2><strong>高野様</strong>について</h2>

            <p><strong>山形</strong><br>本日はよろしくお願いします。</p>

            <p><strong>高野様</strong><br>よろしくお願いします。</p>

            <p><strong>山形</strong><br>まず初めに自己紹介をお願いします。</p>

            <p><strong>高野様</strong><br>スタートアップベンチャー転職のキープレイヤーズ代表です。165社の上場支援、創業またはシードから投資して7社上場の実績を持っています。エンジェル投資家でもあります。</p>

            <p><strong>山形</strong><br>では高野さんの大学時代から就職にかけてのお話をお聞かせください。</p>

            <p><strong>高野様</strong><br>私の両親はどちらも農家出身で、実家は農家でした。祖父や父は社会奉仕を怠らない人で、天皇陛下からも叙勲を受けています。「社会の役に立つことをする」というポリシーなのかなと思ってました。私自身も親に大学の学費等を払ってもらっていたので感謝しています。それを見て育ったので「「自分も何かやらなければ、将来は人助けをするんだ」」と思っていました。それでまずはアルバイトを始めて、その後当時では珍しかったインターンにも参加しました。この時から私は少し周りとは違った価値観を持っていたのかもしれません。周りの人たちは公務員になることや大企業に就職することを目指していることが多く、経営者志望は少なかった印象です。当時、私はいずれ事業を自分で起こしたいと思っていました。自分なりにもトライしましたが、大きくなることはなく、だれも自分のことを知らない状態で自分を試してみたいという気持ちから、地元ではなく都会に出て就活を行おうと思いました。(結果として、)今後伸びそうな企業という観点からインテリジェンス（現パーソル）に就職し、そこで6年間働きその後独立しました。</p>

            <h2>起業の考え方</h2>

            <p><strong>山形</strong><br>高野さんは大学卒業後は起業ではなく就職という手段を取られたとのことですが、それはなぜですか？</p>

            <p><strong>高野様</strong><br>まずは、その当時の私には起業して成功を収めるだけの力がないと感じていたからです。仕事で結果を出して、信頼されることが大事だと思っていました。また、昔は起業する手段も少なかったですね。昔は株式会社は取締役3人、監査役ひとりが必ず必要だったと記憶してます。今とは違って。</p>

            <p><strong>山形</strong><br>今の時代だったら就職ではなく起業してますか？</p>

            <p><strong>高野様</strong><br>起業というか個人事業主でやってみて、月に100万から200万は稼げたらそのまま事業にしたのではないかと。当時もやってみましたが自分ではできませんでした。今は起業しやすくなって、副業とかで起業する人とかも出てきているじゃないですか。でも、結局ほとんどはもうかっていないんですよ。やっぱり就職して実績を出してからでも良いと思います。学生時代に月に200万くらい稼いでる人って今いると思いますが、才能あると思うんですよね。</p>

            <p>大体2000万円を1人で稼げるようになったらだんだんあきてきたり、もっと大きくしたくなる人が多いです。</p>

            <img src="/blog/23_partner_keyplayers_interview/001.jpg" alt="TEDxUTsukubaメンバー(田中)がインタビューを行っている" style="width: 100%; margin: 1rem 0;">

            <h2>学生に向けて</h2>

            <p><strong>山形</strong><br>学生のうちに取り組むと良いことは何だと思いますか？</p>

            <p><strong>高野様</strong><br>まずは何でも良いから興味を持てるものを探すことです。そして、進む業界を決めたら、その業界の中でのトップを目指すことです。そのためにいろんな仕事にトライしましょう。ただ、最初のうちは特に学生の間なんかは何もできないと思われて、相手にしてもらえない、つまり仕事を任せられないと思います。だからこそ、学生のうちは何かに打ち込んで目標をクリアするという経験を積んでほしいですね。何かに打ち込んでいて、自分なりの目標を立てて実行している人は、企業から見ても結構魅力的なんですよ。</p>

            <p><strong>山形</strong><br>他にどんな人材がほしいとかありますか？</p>

            <p><strong>高野様</strong><br>私はギブアンドギブとか利他主義といった言葉が好きです。基本的に人間は自分がかわいいので自分を優先しますが、その中でどれだけ人を優先できるかがその人の価値を決めると思っています。これが社会貢献につながり、自分が社会にいる価値になります。これができるのは自分の目の前のことができていてゆとりがあるからです。このようなゆとりを持ち、今日できること、自分以外の人が喜ぶことをたくさん行うことができる人、それがほしい人材です。</p>

            <img src="/blog/23_partner_keyplayers_interview/002.jpg" alt="TEDxUTsukubaメンバー(高橋)がインタビューを行っている" style="width: 100%; margin: 1rem 0;">

            <h2>TEDxへの協賛理由</h2>

            <p><strong>山形</strong><br>TEDxへの協賛の背景には何がありますか？</p>

            <p><strong>高野様</strong><br>最初はSkyland Venturesの木下さんから、一緒にTEDxという団体に協賛しないかと誘いがあったことです。</p>

            <p><strong>山形</strong><br>そうだったんですね。</p>

            <p><strong>高野様</strong><br>ずっと人助けをしていきたいと思っていて、特に学生の起業や活動に関心がありました。今までは忙しく手が付けられていなかったのですが、今回機会をもらったので彼らに対する支援を始めようかなと考えました。そういう意味では、TEDxへの協賛は、小規模ながら多方面への寄付の一環です。</p>

            <p><strong>山形</strong><br>ありがとうございます！高野さんはTEDxのどんなところに期待していますか？</p>

            <p><strong>高野様</strong><br>学生が表現をする場を設けるという意味で、TEDxの環境は素晴らしいと思います。ただ、もちろんプロの人たちが開催するイベントと比べてしまうと、どうしてもクオリティが落ちてしまうのも確かです。今後は学生たちがクオリティを上げるために本気になりクオリティを上げようとすると、当然それだけ大変になるのですが、それだけ熱意をもって学生が取り組む、そういった環境になることに期待しています。集団としてではなく個人間の関わりとしては、単純に面白いなーって人に会ってみたいですね。登壇者でももちろんそうだし、学生にもそういう人はいるんですよ。</p>

            <h2>一言メッセージ</h2>

            <p><strong>山形</strong><br>最後に大学生に向けて一言お願いします。</p>

            <p><strong>高野様</strong><br>会社に入ってしまったら会社が目標を決め、それに向かって頑張るようになります。だから、目標を決めて実行するのは大学生のうちに経験しなければ経験しづらいものです。でも、社会人になっても自分の目標がなにかを答えられない人はつまらない人になります。逆に目標をはっきり決めて、それに向かって頑張っている人は応援したくなるし、アドバイスもしやすいです。ですから、自分の目標を決めて歩んでいってほしいです。</p>

