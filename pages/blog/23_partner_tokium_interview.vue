                <h2>学生に向けて</h2>

                <p><strong>髙橋</strong>：今の大学生に向けて、TOKIUMさんとして何かコメントを頂きたいです。</p>

                <p><strong>西本様</strong>：今って、めちゃくちゃ選択肢があって皆さんどうやって進路とか決めているんですかね？</p>

                <p><strong>髙橋</strong>：めっちゃ悩んでいます。(笑)</p>

                <p><strong>西本様</strong>：そうですね。学生起業も増えてきたし、YouTuberをやるって人もいるだろうし。エンタメとかそういう業界や業種、ポジションも増えているから、物事を作るのはエンジニア以外もいっぱいあるし...。皆さんどのようなイメージで就活しているんでしょうか？</p>

                <p><strong>髙橋</strong>：そうですね...。個人的な感覚ですが、大学生は3年生から就活をしていると思っています。自分たちの興味を分析してインターンに行くなりしていると思います。想像が足りてない感覚は自分にもありました。</p>

                <p><strong>西本様</strong>：やっぱりやってみないと分からないことが多いと思うんですよ。入学して1年や2年経たないうちに皆さんの考え方は変わると思うんです。ポジティブな方向に変わるかもしれないし、ネガティブに変わるかもしれないですが、とにかく変わるんですよ。</p>

                <p>自分の心や取り巻く環境や見え方、情勢も変わります。変わるという前提に立って、やりたいことを決めて欲しいなと思います。あまり難しく考えずに、自分のファーストインプレッションでもいいんじゃないかなと。その意思決定も大事だけど、入った後で自分がどれだけ頑張れるか、自分が頑張れそうか、も大事だと思う。どれだけがむしゃらでやるか。入ったときにどうするかを考えて動くというのもいいんじゃないかなと思いました。</p>

                <p><strong>髙橋</strong>：そういう意味でいうとTOKIUMさんの志にある「泥臭くやる」というワードと通じているなと感じました。TOKIUMさんとしてはどういう学生を求めているんですか？</p>

                <p><strong>西本様</strong>：志に共感してもらえる人はもちろん、チームのみんなと一緒にお客様、共に働く仲間それぞれにホスピタリティを持ちながら仕事に取り組み、果敢に挑戦できる方を歓迎しています。これからどんどんTOKIUMは伸びていくと思いますし、どんどん変化を遂げていくと思います。そんな会社だからこそ挑戦できる環境を提供できます。</p>

