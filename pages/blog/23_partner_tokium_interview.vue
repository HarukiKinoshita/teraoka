                <p><strong>西本様</strong>：我々は業務効率化、DX*を実現するサービスを提供しています。無駄な時間を減らして、未来へつながる「時間を生み出すインフラ」を目指しています。社名であるTOKIUMも「未来へつながる時を生む」から来ています。</p>

