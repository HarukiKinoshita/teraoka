                <h2>ゲーム開発とキャリアについて</h2>

                <p><strong>山形</strong>：次に経歴についてお聞かせください。</p>

                <p><strong>西本様</strong>：2010年に、筑波大学情報メディア創成学類に入学しました。元々はシンプルにゲームを作りたかったんですよ。ゲームを作る＝プログラミングだ、みたいな。その頃は全然情報系とか詳しくなくて、リテラシーも全然なくて。そういう状況だったので何となくプログラミングができるところに入りたいなという感覚で大学に入って、プログラミングを勉強し始めました。大学外でも、ゲームを簡単なものから作ったりとか、ゲーム開発のバイトをしたりしていました。また、仲間内で筑波大の学祭向けにゲームを作って出すという活動をしていました。</p>

                <p>やっぱりゲーム開発は楽しいなという考えがあったし、大学院に行く選択肢は僕の中でほぼなかったので、ゲーム業界へ就職をしようと思っていました。もっと大きなゲーム開発かつ、多くの人に触ってもらえるような開発をしたいと思って、前職のゲーム会社に入りました。結局3年くらい会社に勤めたのですが、ゲーム開発は厳しいんだなと感じるようになっていきました。</p>

                <p>いくつかプロジェクトに関わったのですが、世に出ずプロジェクトが終了することもしばしば。出しても長続きせずサービス終了というのが続き、たくさんの人と関わったりする中で、「誰とやるか」が重要になっていき、今の会社に入りました。このTOKIUMという会社は筑波大学の同期が立ち上げた会社で、友人関係でもあったし、ずっと声をかけてもらっていたので、シンプルに楽しそうだと思い入社しました。</p>

                <p><strong>山形</strong>：高校のときに文系で、ゲーム作りのために筑波大学を選んだことで、在学中大変だったそうですね。</p>

                <p><strong>西本様</strong>：元々は大阪大学の外国語学部を志望していて、センター入試の結果で変えたんですよね。そのまま受けても良かったし、選んだあとに何をするかの方が重要だと思っていたので、強い希望があって筑波大に入ったわけではないです。センターが終わったあとに数3Cの勉強をして、何とかなりました。(笑)</p>

                <p><strong>山形</strong>：在学中には、ゲーム開発をするために1年間休学したとうかがいました</p>

                <p><strong>西本様</strong>：3年から4年に上がるタイミングで1年間休学をしました。ゲーム開発のバイトをその1年間にがっつりやって、何か自分でゲームを作ったというよりかは、会社に入ってもうめちゃくちゃやりました。</p>

