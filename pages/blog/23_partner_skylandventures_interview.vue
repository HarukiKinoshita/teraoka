        <section class="section">
        <article class="article">
            <p>今回はSkyland Ventures（<a href="https://twitter.com/skylandvc">@skylandvc</a>）の木下慶彦様（<a href="https://twitter.com/kinoshitay">@kinoshitay</a>）へインタビューさせていただきました。会社についてやTEDx団体についてのお話、そして今の学生へ向けたコメントを頂戴いたしました。</p>

            <h2>Skyland Venturesについて</h2>

            <p><strong>田中</strong>：<br>
            まずは簡単に、Skyland Venturesの事業内容について教えてください。</p>

            <p><strong>木下様</strong>：<br>
            Skyland Ventures はベンチャーキャピタル（VC）で、2012年から11年以上スタートアップに対する投資を行っています。主に25歳くらいまでの方に投資をしていて、半数くらいは学生起業家です。そして、「起業しろ」というメッセージを発信しています。
            肌感としては、日本という環境では才能がある人でも自分でプロジェクトをやるということがとても難しく、そのメンタリティがあってもやっていない人が多いと感じています。ここ3年くらいでその状況は変化してきた気もしますが、起業は一般的な概念になってはいないと感じますね。特に地方大学は、東京の感覚とは違うからその行動が起こりにくいように思っています。自分は若い人が起業した方が良いし、学生起業が最高だとも思います。</p>

            <p><strong>田中</strong>：<br>
            Skyland Venturesが達成しようと思っていることはなんですか？</p>

            <p><strong>木下様</strong>：<br>
            起業やスタートアップがマス化することです。日本だと時間はかかると思いますが、これはすごく意味のあることだと思っています。</p>

            <p><strong>田中</strong>：<br>
            なるほど、ありがとうございます。Skyland Venturesではインターン生を積極的に受け入れていると伺いましたが、何か期待していることやその理由等はありますか？</p>

            <p><strong>木下様</strong>：<br>
            インターン生の進路は起業ばかりかというとそうではなく、VCの企業もいますし、大手企業など様々です。前提の考えとして、大学時代にVCにたどり着いたりスタートアップやVCでバイト・インターンをしたりしている人はまず優秀だと思っています。ですので、彼らに機会提供をしたいという思いがあるというのが1つです。
            ただ、単純に機会提供というだけでもなく、その中からVCに1人でも良いから出てきてくれたらな、という思いもあります。将来的にも Skyland Venturesで働いてくれたら自分としては嬉しくもありますが、もう少し広範囲で捉えていて「VCやスタートアップをしてみよう」と思ってくれたら大成功みたいなところはあります。その中で Skyland Ventures がいいのか他の企業がいいのか、こだわりをもって選んでほしいと考えます。
            こういう仕事やこういう業界は中々浸透していない部分もあるので、まずは知ってもらって、好きになってもらえるっていうのが大事。それがTEDxに関わった理由の1つでもあります。スタートアップとかVCって普通はなかなか興味を持ちにくいと思うんですよ。でも「こういうのも良いのではないか」と色々な人に思ってほしいです。</p>

            <p><strong>富田</strong>：<br>
            VCを広く知ってほしいという思いがあるのですね。先ほど学生で起業するのが最高だとも仰っていましたが、学生で起業する一番の魅力は何だとお考えですか？</p>

            <p><strong>木下様</strong>：<br>
            大学生は「大学に行けば同世代の人が大勢いる」という環境があるので、「仲間が多い」ことが大きな魅力だと思いますね。その環境がある学生の方が人との関わりを持ちやすいんじゃないかと思います。社会人になると仲間と出会う機会が少なくなって、より断絶感を感じることが多くなると思うんですよ。学生はサークルとかで共通の趣味を持つ人と出会いやすい環境があっても、社会人になるとそういうのが身近でなくなるというのも一つの要因だと考えています。あとは高校卒業後大学に行って、そして大学を卒業して就職をして、というように大人になっていくほど皆すごく落ち着いて「普通の人」になっていくなという感覚があります。年齢を重ねるにつれて野心が無くなっていく世界、だからこそ若手に起業してほしいんです。肌感としては30歳を超えたら動かなくなる人が多くなるように感じます。</p>

            <p><strong>田中</strong>：<br>
            ありがとうございます。個人的にはある程度ビジネスの経験がある状態で起業をした方が成功しやすいのではないかと考えてしまうのですが、そういった経験を積んでから起業というよりも、若いときから起業という考え方が新鮮に感じます。</p>

            <p><strong>木下様</strong>：<br>
            例えばChatGPTとかってこの1年くらいのもので、その前からこれが世に出ることを言ってた人なんていなくて。もっと身近になっているスマホに関しても同じで、スマホが出てくることを前から予言していた人なんていないですよね。自分の成長するスピードよりも世の中の成長のスピードの方が早くて予想が付かないことも多いから、行動してみることは意義のあることだと思います。</p>

            <p><strong>田中</strong>：<br>
            そういったお考えだったのですね。他の記事で「新卒で会社に入った人のことを即戦力だと思う」と仰っているのを拝見しました。今の日本では「新卒は入社してから育てていく」という考え方が多い気がするのですが、このようにお考えの理由を教えていただけますか？</p>

            <p><strong>木下様</strong>：<br>
            そうですね…でも、新卒を戦力として期待しないのって日本だけだと思いますよ。機会を提供すれば人は成長する、というのが根本の考え方にあります。誰かに育ててもらうというよりは、自分で育っていくんじゃないですかね。</p>

            <img src="/blog/23_partner_skylandventures_interview/001.jpg" alt="木下様の話を伺う富田と田中" style="width: 100%; margin: 1rem 0;">

            <h2>今までの経験について</h2>

            <p><strong>田中</strong>:<br>
            次に、木下さん自身について伺いたいと思います。若いときから様々なことをやられているかと思うのですが、その中で経験した辛かったことや失敗などはありますか？</p>

            <p><strong>木下様</strong>：<br>
            あまり失敗として印象に残っていることはないですね…。VCって投資の仕事をしているんですけど、大前提として僕自身のお金でやっているわけではないんですよ。他の人のお金を預けていただいて、それを元手にやっている仕事なんです。それはスタートアップも同じで、スタートアップも投資家さんのお金でやってることが多いです。分かりやすく、自分は「他力」と「自力」という言葉をよく使っていて、今自分がやっているような事業は他力に分類されると思っています。ちょっとしたミスはあっても、他力でのビジネスなのでそれが辛いと思ったことは一度もないです。</p>

            <p><strong>田中</strong>：<br>
            では、VCをやって良かったと思うことは何ですか？</p>

            <p><strong>木下様</strong>：<br>
            VCに限った話ではないのですが、起業している人は結構ポジティブな人が多いです。この、ポジティブな人が多いっていうのがこの仕事の魅力の1つなのではないかなと思います。そして、スタートアップ関連の周りの人も魅力的な人達が多いなと思います。</p>

            <p><strong>田中</strong>：<br>
            確かに、ポジティブな人と一緒にいると自然とポジティブになりますよね。ちなみに、今たくさんの経験や知識をお持ちだと思うのですが、もし今のこの知識を持って大学生に戻れるとしたらどういうことをしたいと思いますか。</p>

            <p><strong>木下様</strong>：<br>
            なるほど…大学生に戻るとしたら。元々ぼんやりと起業したいっていうのはあったんですけど、どの業界で起業すればいいのか分からなくて。VCは社会的にも意味のある仕事であって、色々な起業家と出会えて良さそうだなと思って始めました。もしも大学生のうちに良い業界を見つけられるとしたら起業したいと思うかもしれないですね。</p>

            <h2>TEDxU団体について</h2>

            <p><strong>富田</strong>：<br>
            続いて、今回多くのTEDxU団体に協賛をされていましたがその理由をお聞かせください。</p>

            <p><strong>木下様</strong>：<br>
            TEDxっていう枠組みが良い取り組みだとインターンのメンバーに聞いたのをきっかけに、スポンサーをすることにしました。個人的には、大学内で世の中に影響のあることをしようとするのがいいなと感じました。</p>

            <p><strong>富田</strong>：<br>
            ありがとうございます。様々なTEDx団体のメンバーにお会いする機会があったかと思うのですが、団体の印象はどうでしょうか？</p>

            <p><strong>木下様</strong>：<br>
            お会いしたTEDxU団体は女性率が高くて驚きました。スタートアップのイベントって女性率2，3割くらいなので、自分にとってはすごく新鮮な環境でしたね。女性代表がいるかどうかで女性比率って変わってくると思うんですけど、TEDxU団体には女性代表が多くいますよね。女性に限らず、基本的には組織には様々な人がいた方が良いと思います。</p>

            <p><strong>富田</strong>：<br>
            最後に学生に向けてメッセージをお願いします。</p>

            <p><strong>木下様</strong>：<br>
            学生のうちはTEDxを含めた様々なイベントから人が動いていくことが多いと思うので、イベントに参加してみて、そこで出会った人達とつながり、そして居場所をつくっていくと良いと思います。</p>

            <p><strong>富田</strong>：<br>
            これで以上となります。本日はお忙しい中お時間をとって頂きありがとうございました！</p>


